#!/usr/bin/env node
/**
 * Rebuild the runtime-independent Master DPS anchors from the deterministic
 * headless damage lab. Run `npm --prefix packages/sim run build` first.
 *
 * Default/`--check` verifies the committed generated module. `--stdout`
 * prints the proposed module, and `--write` deliberately updates it.
 */
import { readFile, writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { runMasterDamageLab } from '../dist/src/index.js';

const args = new Set(process.argv.slice(2));
const supported = new Set(['--check', '--stdout', '--write']);
for (const arg of args) {
  if (!supported.has(arg)) throw new Error(`Unknown argument: ${arg}`);
}
if (args.has('--stdout') && args.has('--write')) {
  throw new Error('Choose --stdout or --write, not both.');
}

const utilityIds = new Set([
  'master:puffer-pouch',
  'master:bat-ears',
  'master:armadillo-greaves',
]);
const results = runMasterDamageLab();
if (results.length !== 12) throw new Error(`Expected 12 Master profiles, received ${results.length}`);
for (const result of results) {
  const utility = utilityIds.has(result.id);
  if (utility) {
    if (result.status !== 'utility-confirmed' && result.status !== 'damage-confirmed') {
      throw new Error(`Master utility profile failed: ${result.id} (${result.status})`);
    }
  } else if (result.status !== 'damage-confirmed') {
    throw new Error(`Master damage profile failed: ${result.id} (${result.status})`);
  }
}

const rows = results.map((result) => {
  const traitId = result.id.slice('master:'.length);
  return `  '${traitId}': ${result.damagePerSecond},`;
});
const source = [
  '/**',
  ' * Generated by packages/sim/scripts/generate-master-dps.mjs from the',
  ' * deterministic 20-second authoritative Master Damage Lab. Do not hand-edit;',
  ' * rerun the generator after changing a Master profile or combat semantics.',
  ' */',
  '',
  'export const MASTER_DPS = Object.freeze({',
  rows.join('\n'),
  '} as const);',
  '',
  'export type MasterDpsTraitId = keyof typeof MASTER_DPS;',
  '',
  'const MASTER_DPS_VALUES: readonly number[] = Object.values(MASTER_DPS);',
  '',
  '/** Stable generated-content input consumed by the runtime/replay fingerprint. */',
  'export const MASTER_DPS_FINGERPRINT_INPUT = Object.entries(MASTER_DPS)',
  '  .map(([traitId, dps]) => `${traitId}:${dps}`)',
  "  .join('|');",
  '',
  'export const MEAN_MASTER_DPS = MASTER_DPS_VALUES',
  '  .reduce((total, value) => total + value, 0) / MASTER_DPS_VALUES.length;',
].join('\n') + '\n';
const target = fileURLToPath(new URL('../../trait-runtime/src/chimera/master-dps.generated.ts', import.meta.url));

if (args.has('--stdout')) {
  process.stdout.write(source);
} else if (args.has('--write')) {
  await writeFile(target, source, 'utf8');
  process.stdout.write(`Wrote ${target}\n`);
} else {
  const current = await readFile(target, 'utf8');
  if (current !== source) {
    throw new Error('Master DPS module is stale; run with --stdout to review or --write to refresh it.');
  }
  process.stdout.write('Master DPS generated module is current.\n');
}
